    private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed
        // TODO add your handling code here:
        String studentName = (String) StudentName.getSelectedItem();
        String batchCode = (String) jComboBatch.getSelectedItem();
        //String book = (String) jComboBook.getSelectedItem();
        //String session = (String) jComboSession.getSelectedItem();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String date = sdf.format(jDateofAttendance.getDate());
        String status = jRadioPresent.isSelected() ? "Present" : "Absent";

        if (studentName.equals("Student")) {
            JOptionPane.showMessageDialog(this, "Please select a student name.");
            return;
        }

        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/attendance", "root", "");
            PreparedStatement ps = con.prepareStatement("INSERT INTO std_attend (std_name, attend_date, std_status, std_batch_Code) VALUES (?, ?, ?, ?)");
            ps.setString(1, studentName);
            ps.setString(2, date);
            ps.setString(3, status);
            ps.setString(4, batchCode);
            //ps.setString(5, book);
           // ps.setString(6, session);

            int res = ps.executeUpdate();
            if (res > 0) {
                JOptionPane.showMessageDialog(this, "Attendance record submitted successfully.");
            } else {
                JOptionPane.showMessageDialog(this, "Failed to submit attendance record.");
            }

            ps.close();
            con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_SubmitActionPerformed

