        private void populateStudentNames() {
       ArrayList<String> studentNames = new ArrayList<>();
       studentNames.add("Select Student Name");
            
        Connection conn = null;
        Statement stmt = null;
        ResultSet rs = null;

        try {
            // Establish the database connection
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/attendance", "root", "");
            stmt = conn.createStatement();
            rs = stmt.executeQuery("SELECT std_name FROM tbl_std");

            // Fetch all student names and add to the list
            while (rs.next()) {
                studentNames.add(rs.getString("std_name"));
            }

            // Set the combo box model with the fetched student names
            StudentName.setModel(new DefaultComboBoxModel<>(studentNames.toArray(new String[0])));

        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                if (rs != null) rs.close();
                if (stmt != null) stmt.close();
                if (conn != null) conn.close();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

     
     private void populateBatchCodes() {
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/attendance", "root", "");
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT batch_code FROM tbl_batch");

            ArrayList<String> batchCodes = new ArrayList<>();
            batchCodes.add("Select Batch Code");
            while (rs.next()) {
                batchCodes.add(rs.getString("batch_code"));
            }

            jComboBatch.setModel(new DefaultComboBoxModel<>(batchCodes.toArray(new String[0])));

            rs.close();
            stmt.close();
            con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }


